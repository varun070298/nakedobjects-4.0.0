package org.nakedobjects.runtime.testsystem;

public class TestProxyException extends RuntimeException {
    private static final long serialVersionUID = 1L;

    public TestProxyException() {
        super();
    }

    public TestProxyException(final String message, final Throwable cause) {
        super(message, cause);
    }

    public TestProxyException(final String message) {
        super(message);
    }

    public TestProxyException(final Throwable cause) {
        super(cause);
    }

}

